function M.setup()
  for group, spec in pairs(M.highlights) do
    vim.api.nvim_set_hl(0, group, spec)
  end
end

function M.section()
  M.setup()
  local text = {}
  for i, line in ipairs(M.lines) do
    for _, chunk in ipairs(line) do
      text[#text + 1] = chunk
    end
    if i < #M.lines then
      text[#text + 1] = {"\n"}
    end
  end
  return { align = "center", padding = 1, text = text }
end

